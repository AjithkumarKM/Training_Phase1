package phase1.programs;

public class Datatypes {

	public static void main(String[] args){
		
		int age = 56;
		System.out.println("Age is: " + age);
		
		float pi = 3.147f;
		System.out.println("Pi value is: " + pi);
		
		double dist = 13672.7687;
		System.out.println("Distance is: " + dist);
		
		char gender = 'M';
		System.out.println("Gender is: " + gender);
		
		byte b = 23;
		System.out.println("Btye value is: " + b);
		
		boolean bool = true;
		System.out.println("Boolean value is: " + bool);
		
		short val = 256;
		System.out.println("Short value is: " + val);
		
		long lval = 124567L;
		System.out.println("Long value is: " + lval);
	}
}
